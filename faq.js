let isOpen = false;
let faq1 = document.getElementById("faq_1");

faq1.addEventListener("click", function() {
    if(isOpen) {
    document.querySelectorAll('.video-card').forEach(c => {
        if(c.style.display === "block"){
        c.style.display = 'none'
        } 
    })
    faq1.querySelector('.video-card').style.display = 'block';

    } else {
    faq1.querySelector('.video-card').style.display = 'block';

    }
    isOpen = !isOpen;
})

